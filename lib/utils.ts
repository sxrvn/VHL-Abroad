export function formatDate(date: string | Date): string {
  const d = new Date(date);
  return d.toLocaleDateString('en-US', {
    year: 'numeric',
    month: 'short',
    day: 'numeric',
  });
}

export function formatDateTime(date: string | Date): string {
  const d = new Date(date);
  return d.toLocaleString('en-US', {
    year: 'numeric',
    month: 'short',
    day: 'numeric',
    hour: '2-digit',
    minute: '2-digit',
  });
}

export function isBatchExpired(endDate: string): boolean {
  return new Date(endDate) < new Date();
}

export function getDaysRemaining(endDate: string): number {
  const end = new Date(endDate);
  const now = new Date();
  const diff = end.getTime() - now.getTime();
  return Math.ceil(diff / (1000 * 60 * 60 * 24));
}

export function addMonths(date: Date, months: number): Date {
  const result = new Date(date);
  result.setMonth(result.getMonth() + months);
  return result;
}

export function formatDuration(minutes: number): string {
  const hours = Math.floor(minutes / 60);
  const mins = minutes % 60;
  if (hours === 0) return `${mins}m`;
  if (mins === 0) return `${hours}h`;
  return `${hours}h ${mins}m`;
}

export function getClassStatus(scheduledAt: string, durationMinutes: number): 'upcoming' | 'live' | 'ended' {
  const now = new Date();
  const scheduled = new Date(scheduledAt);
  const endTime = new Date(scheduled.getTime() + durationMinutes * 60000);
  
  if (now < scheduled) return 'upcoming';
  if (now >= scheduled && now <= endTime) return 'live';
  return 'ended';
}
